# Polymorphism
# Polymorphism means "many forms" – the same operator or function behaves differently depending on the data type.

# Built-in polymorphism
a=10
b=20
print(a+b)              # + adds two integers → 30
print(str(a)+str(b))    # + concatenates two strings → "1020"
print([1,2]+[3,4])      # + merges two lists → [1, 2, 3, 4]
